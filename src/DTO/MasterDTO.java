package DTO;

public class MasterDTO {
	String masterId;
	String masterPd;
	String masterOk;
	String sName;
	int sBirth;
	String sGender;
	String sPhone;
	
	public String getMasterId() {
		return masterId;
	}
	public void setMasterId(String masterId) {
		this.masterId = masterId;
	}
	public String getMasterPd() {
		return masterPd;
	}
	public void setMasterPd(String masterPd) {
		this.masterPd = masterPd;
	}
	public String getMasterOk() {
		return masterOk;
	}
	public void setMasterOk(String masterOk) {
		this.masterOk = masterOk;
	}
	public String getsName() {
		return sName;
	}
	public void setsName(String sName) {
		this.sName = sName;
	}
	public int getsBirth() {
		return sBirth;
	}
	public void setsBirth(int sBirth) {
		this.sBirth = sBirth;
	}
	public String getsGender() {
		return sGender;
	}
	public void setsGender(String sGender) {
		this.sGender = sGender;
	}
	public String getsPhone() {
		return sPhone;
	}
	public void setsPhone(String sPhone) {
		this.sPhone = sPhone;
	}
	
}
